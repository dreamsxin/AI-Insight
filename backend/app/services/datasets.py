_DATASETS = {
    "xor": _xor,
    "moons": _moons,
    "circles": _circles,
    "spiral": _spiral,
    "digits": _digits,
}


def list_datasets() -> list[dict]:
    """Return lightweight summaries for all datasets."""
    summaries = []
    for name, fn in _DATASETS.items():
        ds = fn()
        summaries.append({
            "name": name,
            "description": ds["description"],
            "sample_count": len(ds["data"]),
            "input_dim": ds["input_dim"],
            "output_dim": ds["output_dim"],
            "n_classes": ds["n_classes"],
            "suggested_layers": ds["suggested_layers"],
        })
    return summaries


def get_dataset(name: str) -> dict:
    """Return the full dataset by name. Raises ValueError if unknown."""
    fn = _DATASETS.get(name)
    if fn is None:
        raise ValueError(f"Unknown dataset: {name}")
    return fn()
